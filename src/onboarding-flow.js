function getCurrentMenuPrompt(flow) {
  if (flow.state === 'top_menu') {
    return `🤖 *ברוך הבא לבוטי — תפריט עזרה*\n\nאני עוזר אישי חכם בוואטסאפ של מושיקו. בחר נושא ואסביר מה אפשר לעשות:`;
  }
  if (flow.state === 'category_view') {
    const cat = CATEGORY_CONTENT[flow.currentCategory];
    if (!cat) return '⚠️ קטגוריה לא ידועה';
    return `${cat.title}\n\n${cat.body}\n\n━━━━━━━━━━━━━━━━━━━━`;
  }
  return '';
}

function getCurrentMenuOptions(flow) {
  if (flow.state === 'top_menu') return TOP_MENU_OPTIONS;
  if (flow.state === 'category_view') return POST_VIEW_OPTIONS;
  return [];
}

/**
 * Apply a vote on the current menu poll. Returns the next action.
 */
function applyVote(flow, selectedLabel) {
  const opts = getCurrentMenuOptions(flow);
  const opt = opts.find(o => o.label === selectedLabel);
  if (!opt) return { error: `לא זיהיתי "${selectedLabel}"` };

  if (opt.id === 'exit') {
    return { exit: true };
  }
  if (opt.id === 'back') {
    flow.state = 'top_menu';
    flow.currentCategory = null;
    return { ok: true };
  }
  // Otherwise — opt.id is a category. Show its content.
  if (CATEGORY_CONTENT[opt.id]) {
    flow.state = 'category_view';
    flow.currentCategory = opt.id;
    return { ok: true };
  }
  return { error: `קטגוריה לא ידועה: ${opt.id}` };
}

