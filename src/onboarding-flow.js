/**
 * Onboarding / Help menu — interactive guided tour of bot capabilities.
 *
 * Triggered by: "תפריט", "עזרה", "מה אתה יכול", "/help", "ברוכים הבאים", "?"
 *
 * Two-level menu:
 *   - Top menu (poll): pick a category
 *   - Per-category info screen (text) + sub-poll: "back to menu" / "exit"
 *
 * Same poll mechanics as interactive-flow.js but simpler — no data
 * collection, just navigation + content rendering.
 */

const FLOW_TIMEOUT_MS = 15 * 60 * 1000; // 15 minutes — enough to read content

const flows = new Map(); // chatId → flow

function getFlow(chatId) {
  const f = flows.get(chatId);
  if (!f) return null;
  if (Date.now() - f.lastActivity > FLOW_TIMEOUT_MS) {
    flows.delete(chatId);
    return null;
  }
  return f;
}

function startFlow(chatId) {
  const f = {
    chatId,
    type: 'onboarding',
    state: 'top_menu', // 'top_menu' | 'category_view'
    currentCategory: null,
    activePollId: null,
    createdAt: Date.now(),
    lastActivity: Date.now(),
  };
  flows.set(chatId, f);
  return f;
}

function updateFlow(chatId, updates) {
  const f = flows.get(chatId);
  if (!f) return null;
  Object.assign(f, updates);
  f.lastActivity = Date.now();
  return f;
}

function endFlow(chatId) {
  flows.delete(chatId);
}

